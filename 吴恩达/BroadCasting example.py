import numpy as np
A = np.array([[56,0,4.4,68],[1.2,104,52,8],[1.8,135,99,0.9]])
print(A)
print("--------------------------------------------------------------------")
#对每一列求和
sum = A.sum(axis=0)
print(sum)
print("--------------------------------------------------------------------")
#reshape表示将sum重塑为1行4列,将3行4列的矩阵A除以这样的一个1行4列矩阵
percentage = 100*A / (sum.reshape(1,4)) #reshape矩阵非常好用，O(1)复杂度
print(percentage)
print("--------------------------------------------------------------------")